Utf8Char toLower(Utf8Char c) {
    if (auto f = toLowerMap.find(c); f != toLowerMap.end()) {
        return Utf8Char{f->second};
    }

    return c;
}

Utf8Char toUpper(Utf8Char c) {
    if (auto f = toUpperMap.find(c); f != toLowerMap.end()) {
        return Utf8Char{f->second};
    }

    return c;
}

Utf8Char toggleCase(Utf8Char c) {
    if (auto f = toLowerMap.find(c); f != toLowerMap.end()) {
        return Utf8Char{f->second};
    }

    if (auto f = toUpperMap.find(c); f != toLowerMap.end()) {
        return Utf8Char{f->second};
    }

    return c;
}

FString toLower(FString str) {
    for (auto &c : str) {
        c.c = toLower(c.c);
    }
    return str;
}

FString toUpper(FString str) {
    for (auto &c : str) {
        c.c = toUpper(c.c);
    }
    return str;
}

FString toggleCase(FString str) {
    for (auto &c : str) {
        c.c = toggleCase(c.c);
    }
    return str;
}

